package org.ciapge.config.spring.config;

/**
 * @author 朱林
 * @description
 * @date 2021/11/25 22:50
 */
public class ServerConfig {
    private String host;
    private String port;

    public String getHost() {
        return host;
    }

    public void setHost(String host) {
        this.host = host;
    }

    public String getPort() {
        return port;
    }

    public void setPort(String port) {
        this.port = port;
    }
}
